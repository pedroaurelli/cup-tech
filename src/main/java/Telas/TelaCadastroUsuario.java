  private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
    // TODO add your handling code here:
    String nome = nomeTextField.getText();
    String senha = new String (senhaPF.getPassword()) ;
    String confirmaSenha = new String (confirmaSenhaPF.getPassword());
    
    System.out.println(senha);
    System.out.println(confirmaSenha);
    
    if (!confirmaSenha.equals(senha)) {
      JOptionPane.showMessageDialog(null, "Senhas devem ser iguais!");
    }
    
    try {
      Usuario usuario = new Usuario(nome, senha);
      UsuarioDAO usuarioDAO = new UsuarioDAO();
      
      usuarioDAO.cadastrar(usuario);
      
      JOptionPane.showMessageDialog(null, "Cadastro realizado! Fa√ßa login.");
 
      TelaLogin login = new TelaLogin();
      login.setVisible(true);
      this.dispose();
      
    } catch (Exception e) {
      JOptionPane.showMessageDialog(null, "Erro ao cadastrar");
    }
  }//GEN-LAST:event_btnCadastrarActionPerformed

